# Hello World - Python's entry point
print("Hello World!")

# Variables: No 'int' or 'String' declaration; Python infers types
name = "Sudarshan Patil H J"  # String (str)
age = 18  # Integer (int) - like Java but no 'int age = 19;'
height = 5.7  # Float (decimal)
is_student = True  # Boolean (bool)

# Print with formatting (f-strings, easier than Java's + or printf)
print(f"Hello, {name}! You are {age} years old, {height}m tall, and a student: {is_student}")

gym_days=6
print(f"I work {gym_days} days in a week")

print(f"{name} , age {age}")
age="eighteen"
print(age)
